    public Repair open(Repair repair) {
        if (repair == null) {
            close();
        }
        this.repair = repair;
        ArrayList<RepairType> repairTypes = Model.getModel().getRepairTypes();
        if (repairTypes != null) {
            for (RepairType repairType : repairTypes) {
                cRepair.addItem(repairType);
            }
        }

        ArrayList<RoadType> roadTypes = Model.getModel().getRoadTypes();
        if (roadTypes != null) {
            for (RoadType roadType : roadTypes) {
                cRoadType.addItem(roadType);
            }
        }
        cRepair.setSelectedItem(repair.getRepairType());

        if (repair.getRoad() != null) {
            repairOuRoad(true);
            cRoadType.setSelectedItem(repair.getRoad().getRoadType());
            cRoad.setSelectedItem(repair.getRoad());
        } else {
            repairOuRoad(false);
        }
        fDateBegin.setText(Utils.convertDateToStr(repair.getDateBegin()));
        tComment.setText(repair.getComment());
        setVisible(true);
        return this.repair;
    }

    private void repairOuRoad(boolean b) {
        cOnRoad.setSelected(b);
        cRoadType.setEnabled(b);
        cRoad.setEnabled(b);
    }

    private void updateRoad(RoadType roadType) {
        cRoad.removeAllItems();
        ArrayList<Road> roads = Model.getModel().getRoadsByType(roadType);
        if (roads != null) {
            for (Road road : roads) {
                cRoad.addItem(road);
            }
        }
    }

    private void save() {
        repair = new Repair(
                this.repair.getIdRepair(),
                (RepairType) cRepair.getSelectedItem(),
                this.repair.getCar(),
                (cOnRoad.isSelected() ? (Road) cRoad.getSelectedItem() : null),
                this.repair.getDateBegin(),
                this.repair.getDateEnd(),
                tComment.getText().trim()
        );
        setVisible(false);
    }

    private void endRepair() {
        int i = JOptionPane.showConfirmDialog(this, "Вы уверены, что ремонт завершен?", "Внимание...", JOptionPane.YES_NO_OPTION);
        if (i == 0) {
            Repair r = this.repair;
            close();
            controllerCar.endRepair(r.getCar());

        }
    }

    private void close() {
        this.repair = null;
        setVisible(false);
    }

